int lookupChar(char c)
{
    // Direct mapping for the problematic characters
    if (c == '!') return 54;
    if (c == ',') return 55;
    if (c == '.') return 56;
    
    // Regular lookup for other characters
    for (int i = 0; i < CHARAMOUNT; i++) 
    {
        if (c == lookup[i])
        {
            return i;
        }
    }
    return 0;
}




